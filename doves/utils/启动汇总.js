/**
 * @file 启动汇总
 * @description 启动阶段日志静默 + 汇总打印工具
 * 
 * 设计：
 * 1. 启动期间设置静默标志，各模块检查后跳过非关键日志
 * 2. 收集启动统计数据（扩展、技能、工具、能力等）
 * 3. 启动完成后打印汇总表格
 */

// ==================== 静默模式 ====================

let _silent = false;

/** 启用静默模式（启动期间调用） */
export function 启用静默() { _silent = true; }

/** 关闭静默模式（启动完成后调用） */
export function 关闭静默() { _silent = false; }

/** 当前是否静默 */
export function 是否静默() { return _silent; }

// ==================== 统计收集 ====================

const _汇总 = {
  开始时间: 0,
  结束时间: 0,
  鸽子数: 0,
  能力: { 总数: 0, 模型: 0, 技能: 0, 工具: 0, 平台: 0, MCP: 0 },
  不可用: [],
  扩展: [],        // { 名称, 状态, 技能数, 工具数, 模式 }
  扩展成功: 0,
  扩展失败: 0,
  服务端地址: '',
  机器标识: '',
  残留任务: 0,
  加密: false,
  直连端口: null,
};

/** 记录启动开始时间 */
export function 开始计时() { _汇总.开始时间 = Date.now(); }

/** 记录启动结束时间 */
export function 结束计时() { _汇总.结束时间 = Date.now(); }

/** 获取启动耗时（毫秒） */
export function 启动耗时() { return _汇总.结束时间 - _汇总.开始时间; }

/** 记录鸽子数量 */
export function 记录鸽子数(n) { _汇总.鸽子数 = n; }

/** 记录能力统计 */
export function 记录能力(统计) {
  _汇总.能力 = { ..._汇总.能力, ...统计 };
}

/** 记录不可用项 */
export function 记录不可用(列表) { _汇总.不可用 = 列表; }

/** 记录扩展加载结果 */
export function 记录扩展(名称, { 状态, 技能数 = 0, 工具数 = 0, 模式 = '' } = {}) {
  _汇总.扩展.push({ 名称, 状态, 技能数, 工具数, 模式 });
  if (状态 === '成功') _汇总.扩展成功++;
  else _汇总.扩展失败++;
}

/** 记录服务端地址 */
export function 记录服务端地址(url) { _汇总.服务端地址 = url; }

/** 记录机器标识 */
export function 记录机器标识(id) { _汇总.机器标识 = id; }

/** 记录残留任务 */
export function 记录残留任务(n) { _汇总.残留任务 = n; }

/** 记录加密连接 */
export function 记录加密(v) { _汇总.加密 = v; }

/** 记录直连端口 */
export function 记录直连端口(port) { _汇总.直连端口 = port; }

// ==================== 汇总打印 ====================

/**
 * 打印启动汇总表格
 */
export function 打印汇总() {
  const 耗时秒 = ((_汇总.结束时间 - _汇总.开始时间) / 1000).toFixed(1);
  const { 能力, 鸽子数, 扩展, 不可用, 服务端地址, 机器标识, 残留任务, 加密, 直连端口 } = _汇总;

  console.log('');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log(`  白鸽启动完成  ${耗时秒}s`);
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  
  // 基础信息
  console.log(`  服务端    ${服务端地址}`);
  console.log(`  机器标识  ${机器标识}`);
  console.log(`  鸽子实例  ${鸽子数}`);
  console.log(`  加密通道  ${加密 ? '已建立' : '未启用'}`);
  if (直连端口) console.log(`  直连端口  ${直连端口}`);
  if (残留任务 > 0) console.log(`  残留回收  ${残留任务}个`);
  console.log('');

  // 能力统计
  console.log(`  能力  ${能力.总数}项 ┃ 模型${能力.模型}  技能${能力.技能}  工具${能力.工具}  平台${能力.平台}  MCP${能力.MCP}`);
  if (不可用.length > 0) {
    console.log(`  不可用    ${不可用.length}项: ${不可用.join(', ')}`);
  }
  console.log('');

  // 扩展加载
  if (扩展.length > 0) {
    console.log(`  扩展  ${_汇总.扩展成功}成功/${_汇总.扩展失败}失败`);
    for (const ext of 扩展) {
      const icon = ext.状态 === '成功' ? '✓' : '✗';
      const parts = [ext.名称];
      if (ext.模式) parts.push(`[${ext.模式}]`);
      if (ext.技能数 > 0) parts.push(`${ext.技能数}技能`);
      if (ext.工具数 > 0) parts.push(`${ext.工具数}工具`);
      console.log(`    ${icon} ${parts.join(' ')}`);
    }
  } else {
    console.log('  扩展  无');
  }

  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log('');
}
