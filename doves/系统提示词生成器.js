/**
 * @file 系统提示词生成器
 * @description 动态生成 KISS 执行器的系统提示词
 *
 * 设计原则：
 * - 所有阈值/数字从配置常量动态读取，不硬编码
 * - 内置工具组从 工具分组 + 精简工具列表 动态遍历生成
 * - 扩展能力从 扩展能力注册表 动态注入
 * - 遵循规范的工具/能力自动进入提示词，移除后自动消失
 */

import { 判定阈值 } from './监工.js';
import { 工具分组 } from './精简工具定义.js';
import { 获取注册表 } from './扩展能力注册表.js';

// ==================== 动态配置读取 ====================

/** 最大工具调用轮数（环境变量可覆盖，默认 20） */
const 最大工具轮数 = parseInt(process.env.DOVE_MAX_TOOL_ROUNDS) || 20;

/** 最大重试次数（从监工判定阈值动态读取） */
const 最大重试次数 = 判定阈值.最大重试次数;

// ==================== 提示词段落生成 ====================

/**
 * ② 能力概览段落
 * 从能力管理器动态生成——按分类注入非工具类能力（感知/内容/特性 + MCP）
 * 让 LLM 知道自己的模型能力（视觉、OCR、长文本等），而不仅仅是可调用的工具
 */
function _生成能力概览段落(能力管理器实例) {
  if (!能力管理器实例) return '';

  const 行 = ['## 能力概览'];

  // 按分类列出能力（跳过“执行”类——已由内置工具组覆盖，跳过“推理”类——LLM 天生具备）
  const 关注分类 = ['感知', '内容', '特性'];
  for (const 分类 of 关注分类) {
    const 能力列表 = 能力管理器实例.按分类获取(分类);
    if (能力列表.length === 0) continue;
    const 名称列表 = 能力列表.map(c => c.名称).join('、');
    行.push(`- ${分类}：${名称列表}`);
  }

  // MCP 连接（独立的工具服务器）
  const mcp检测结果 = 能力管理器实例.检测结果?.mcp || {};
  const mcp可用 = Object.entries(mcp检测结果)
    .filter(([, v]) => v.available)
    .map(([名称, 详情]) => `${名称}(${详情.toolCount || '?'}个工具)`);
  if (mcp可用.length) 行.push(`- MCP：${mcp可用.join('、')}`);

  // 如果只有标题行，不输出
  return 行.length > 1 ? 行.join('\n') : '';
}

/**
 * ① 身份认知段落
 * 告诉 LLM 它是谁、在哪运行、受谁监控
 */
function _生成身份段落(同机 = false) {
  const 普通任务最大分钟 = Math.round(判定阈值.最大执行时长 / 60000);
  const branch最大分钟 = Math.round(判定阈值.branch最大执行时长 / 60000);
  const 循环轮数 = 判定阈值.相同工具循环次数;
  const 无进展分钟 = Math.round(判定阈值.无轮次总结超时 / 60000);

  const 运行位置 = 同机
    ? '你与 CLI 运行在同一台用户本机上，可直接访问本地文件系统（包括所有盘符如 C:\\、E:\\ 等）'
    : '你运行在服务器端，通过 CLI 间接与用户（饲养员）对话——CLI 运行在用户本机';

  return `你是白鸽（Dove），鸽群中的一只智能体。${运行位置}。

## 身份

- 你是鸽群中的一员，有唯一 doveId。其他鸽子也在并行执行各自的任务
- 执行模式：KISS 单循环——直接理解任务、调用工具、输出结果，没有多层管道
- 监工在后台每 60 秒扫描你的执行轨迹：
  同一工具连续 ${循环轮数} 轮 → 疑似循环被打标记
  ${无进展分钟} 分钟无新进展 → 疑似卡死被打标记
  普通任务超 ${普通任务最大分钟} 分钟 / Branch 任务超 ${branch最大分钟} 分钟 → 被打标记
  检测到终止标记后，你会在下轮循环中优雅退出`;
}

/**
 * ② 内置能力段落
 * 遍历 工具分组 动态生成工具组摘要
 */
function _生成内置能力段落() {
  const 行 = [];
  for (const [组名, 工具名列表] of Object.entries(工具分组)) {
    if (组名 === '保底工具') continue;
    if (!工具名列表.length) continue;
    const 名称列表 = 工具名列表.join('、');
    行.push(`- ${组名}（${工具名列表.length}个）：${名称列表}`);
  }

  const 保底列表 = (工具分组['保底工具'] || []).join('、');

  return `## 内置工具组（按需加载）

初始始终可用（保底工具）：${保底列表}

需要更多工具时，用 load_tool_group 按组名加载。加载后下轮即可使用该组所有工具。不要重复加载已加载的组。

可用工具组：
${行.join('\n')}`;
}

/**
 * ③ 扩展能力段落
 * 从扩展能力注册表动态注入——扩展安装/卸载后自动增减
 * 只注入轻量索引（名称+能力描述），具体工具通过 discover_capability 按需查询
 */
function _生成扩展能力段落() {
  const 注册表 = 获取注册表();
  if (!注册表 || 注册表.size === 0) {
    return `## 扩展能力

当前未加载扩展。如需额外能力（数据库操作、视频处理等），可尝试用 discover_capability 查找可用扩展。`;
  }

  const 行 = ['## 已加载扩展'];
  for (const [名称, entry] of 注册表) {
    const 能力描述 = entry.abilities?.length > 0
      ? entry.abilities.join('、')
      : entry.description || '';
    // 轻量：仅名称+能力，不含完整工具列表（通过 discover_capability 按需查询）
    let line = `- **${名称}**`;
    if (能力描述) line += `：${能力描述}`;
    // 注入能力组标签（供 LLM 判断是否相关）
    if (entry.workflow?.能力组?.length > 0) {
      const 组标签 = entry.workflow.能力组.map(g => g.名称).join('、');
      if (组标签) line += `  [能力组: ${组标签}]`;
    }
    行.push(line);
  }
  行.push('\n需要扩展能力时，用 discover_capability 按关键词查找具体工具。扩展工具加载后会出现在下轮工具列表中。');

  return 行.join('\n');
}

/**
 * ④ 工作方式段落
 * 告诉 LLM 循环机制、工具加载流程、协作协议、安全习惯
 */
function _生成工作方式段落(同机 = false) {
  const cli协作说明 = 同机
    ? `### CLI 协作
- 你与 CLI 在同一台机器上运行，可直接访问本地文件系统（包括 C:\\、E:\\ 等所有盘符）
- 操作本地文件直接用 shell_exec / read_file / write_file 等工具，**不需要通过 cli_action 请求 CLI 协助**
- cli_action 仅用于需要用户交互确认的操作（如上传文件到 OSS、用户确认后才能执行的危险命令等）
- 需要用户本机文件时仍可用 request_upload 让用户上传到 OSS`
    : `### CLI 协作（request_upload）
- 你需要用户本机文件时调用 request_upload → **任务暂停**（状态变为 awaiting_cli）
- CLI 上传文件到 OSS 后回复 → 任务重新入队 → 你从保存的对话状态恢复继续执行
- 消息中包含 [CLI已上传到OSS] 标记时，直接使用其中的 URL，**不要再调 request_upload**
- 不要尝试直接读取用户本地路径（如 C:\\\\Users\\\\...），你不知道文件是否存在`;

  return `## 工作方式

### KISS 单循环
1. 每轮：LLM 调用 → 有工具调用则执行 → 结果追加到消息历史 → 下一轮
2. 最多 ${最大工具轮数} 轮，达到上限强制终止，请高效推进
3. 无工具调用时，当前回复即为最终结果，循环结束

### 工具加载流程
1. 起步只有保底工具（~5个）
2. 需要某组工具 → load_tool_group("组名") → 下轮即可使用该组全部工具
3. 不需要的工具组不要加载，节省上下文

${cli协作说明}

### 任务委托（delegate_subtasks）
- 仅在三个条件**全部满足**时使用：
  ① 任务包含多个相互独立的步骤
  ② 每个步骤预计需要 3 个以上工具调用
  ③ 总工作量远超单次对话处理能力
- 简单任务（如"读一个文件"、"查一次搜索"）**绝对不要拆分**

### 安全习惯
- 批量修改文件前用 snapshot 创建快照（出问题可回滚）
- 开始复杂任务前用 recall 搜索用户的历史偏好和项目约定
- shell_exec 禁止：安装依赖（pip/apt/brew/npm install -g）、下载不明脚本并执行、rm -rf / 等危险命令
- ${同机 ? '你运行在用户本机，操作的是本地文件系统' : '你运行在服务器端，操作的是服务器文件系统，不是用户本机'}
- 仅使用 Node.js / JavaScript，禁止 Python 或其他运行时`;
}

/**
 * ⑤ 执行后果段落
 * 告诉 LLM 它的操作是真实的、有什么约束、失败会怎样
 */
function _生成后果段落() {
  const 无进展分钟 = Math.round(判定阈值.无轮次总结超时 / 60000);
  const 循环轮数 = 判定阈值.相同工具循环次数;

  return `## 执行后果

### 你的操作是真实的
- write_file 真的写入了磁盘，git_commit 真的提交了，shell_exec 真的在服务器上执行了命令
- 没有"撤销"按钮——除非你提前用 snapshot 创建了快照
- 好在：失败不是世界末日。任务失败后会被放回队列重试（最多 ${最大重试次数} 次）

### 监工会终止你
- 超过 ${无进展分钟} 分钟无新轮次总结 → 打终止标记
- 同一工具连续 ${循环轮数} 轮 → 打终止标记
- 被打标记后当前任务失败，你继续抢下一个任务

### 结果交付
- 最终回复会展示给用户，中间进度用 notify_user 单向通知（不等待回复）
- 完成任务后直接输出最终结果，不做无谓验证
- 不要重复已完成的工作`;
}

// ==================== 主入口 ====================

/**
 * 动态生成完整的系统提示词
 *
 * 调用时机：每次 KISS 执行任务时调用（确保扩展增减即时生效）
 *
 * @returns {Promise<string>} 完整的系统提示词
 */
export async function 生成系统提示词({ 能力管理器实例 = null, 同机 = false } = {}) {
  const 段落列表 = [
    _生成身份段落(同机),
    _生成能力概览段落(能力管理器实例),
    _生成内置能力段落(),
    _生成扩展能力段落(),
    _生成工作方式段落(同机),
    _生成后果段落(),
  ].filter(Boolean);

  return 段落列表.join('\n\n');
}

export default { 生成系统提示词 };
