/**
 * AI 决策入口
 * @param {object} 摘要 - 游戏引擎构建的状态摘要
 * @param {string} 难度 - 简单|普通|困难|地狱
 * @returns {Array} 动作列表
 */
export async function AI决策(摘要, 难度 = '普通') {
  const 配置 = AI难度配置[难度] || AI难度配置.普通;

  try {
    const 动作列表 = await LLM决策(摘要, 配置);
    if (动作列表 && 动作列表.length > 0) return 动作列表;
  } catch (e) {
    // LLM 失败，回退规则AI
  }
  return 规则AI决策(摘要, 配置);
}

/**
 * 获取指定难度的决策间隔
 */
export function 获取AI决策间隔(难度 = '普通') {
  return (AI难度配置[难度] || AI难度配置.普通).决策间隔;
}

/**
 * 获取地狱AI的额外收入
 */
export function 获取额外收入(难度 = '普通') {
  return (AI难度配置[难度] || AI难度配置.普通).额外收入 || 0;
}

// ===== LLM 决策 =====

async function LLM决策(摘要, 配置) {
  const prompt = `你是循环塔防游戏的AI玩家。${配置.策略提示}

当前战局状态：
${JSON.stringify(摘要, null, 2)}

可用塔台类型及费用：
- 箭塔(50金) 单体伤害10 射程3 冷却2tick
- 冰塔(75金) 伤害5+减速30% 射程4 冷却3tick
- 炮塔(100金) 范围伤害8 射程2 冷却4tick
- 电塔(150金) 单体伤害20 射程5 冷却5tick
升级费用=底价×等级(1.5/2.5)，属性+40%/级，最高3级

可用兵种及费用：
- 侦察兵(20金) 30HP 速度2/tick 无甲
- 重甲(60金) 120HP 速度0.5/tick 10护甲
- 虫群(10金) 15HP×3只 速度1/tick
- 精英(150金) 200HP 速度1/tick 5护甲+每tick回2血

塔台建造位置：列只能是0或6，行范围1-${段落高度 - 2}。

返回严格JSON数组（1-3个动作，不要markdown标记和多余文字）：
[{"动作":"建造","类型":"箭塔","位置":{"行":3,"列":0}}]
[{"动作":"升级","塔台ID":"e_xxx"}]
[{"动作":"生产","类型":"侦察兵","数量":2}]
[{"动作":"出售","塔台ID":"e_xxx"}]
[{"动作":"等待"}]`;

  const result = await callLLM({
    messages: [{ role: 'user', content: prompt }],
    temperature: 配置.temperature,
    max_tokens: 400,
  });

  if (!result.success) return null;

  const match = result.content.match(/\[[\s\S]*?\]/);
  if (!match) return null;

  const 动作 = JSON.parse(match[0]);
  return Array.isArray(动作) ? 动作 : null;
}

// ===== 规则 AI =====

function 规则AI决策(摘要, 配置) {
  const { 我的状态, 攻击目标 } = 摘要;
  const 动作 = [];
  const 资源 = 我的状态.资源;

  // 紧急防御：来袭多或HP低
  if (我的状态.来袭敌军 > 配置.规则_建塔阈值 || 我的状态.基地HP < 40) {
    if (资源 >= 50 && 摘要.可用塔位数 > 0) {
      const 位置 = 找最优塔位(我的状态);
      if (位置) {
        // 优先选择合适的塔台类型
        let 类型 = '箭塔';
        if (资源 >= 150 && 我的状态.来袭敌军 > 4) 类型 = '电塔';
        else if (资源 >= 100 && 我的状态.来袭敌军 > 3) 类型 = '炮塔';
        else if (资源 >= 75) 类型 = '冰塔';
        动作.push({ 动作: '建造', 类型, 位置 });
      }
    }
  }

  // 升级已有塔台（困难以上AI会主动升级）
  if (配置.规则_建塔阈值 <= 2 && 我的状态.塔台数 >= 3 && 资源 >= 100) {
    const 可升级 = 我的状态.塔台.find(t => !t.includes('Lv3'));
    if (可升级) {
      const idMatch = 可升级.match(/id:(\S+)/);
      if (idMatch) {
        动作.push({ 动作: '升级', 塔台ID: idMatch[1] });
      }
    }
  }

  // 进攻判断
  if (资源 >= 配置.规则_出兵资源 && 我的状态.基地HP > 30 && 攻击目标.目标存活) {
    const 兵种 = 选择进攻兵种(配置, 攻击目标, 资源);
    动作.push(...兵种);
  }

  if (动作.length === 0) 动作.push({ 动作: '等待' });
  return 动作;
}

function 选择进攻兵种(配置, 攻击目标, 资源) {
  const 动作 = [];

  if (攻击目标.目标塔台数 >= 4 && 资源 >= 40) {
    // 重防御：先虫群消耗再重甲突破
    动作.push({ 动作: '生产', 类型: '虫群', 数量: 3 });
    if (资源 >= 100) {
      动作.push({ 动作: '生产', 类型: '重甲', 数量: 1 });
    }
  } else if (攻击目标.目标塔台数 <= 1 && 资源 >= 150) {
    // 弱防御：直接精英突击
    动作.push({ 动作: '生产', 类型: '精英', 数量: 1 });
  } else if (资源 >= 60) {
    // 常规：根据偏好随机选
    const 偏好 = 配置.出兵偏好;
    const 类型 = 偏好[Math.floor(Math.random() * 偏好.length)];
    const 费用 = 兵种配置[类型]?.费用 || 20;
    const 数量 = Math.min(3, Math.floor(资源 * 0.6 / 费用));
    if (数量 > 0) 动作.push({ 动作: '生产', 类型, 数量 });
  }

  return 动作;
}

function 找最优塔位(我的状态) {
  const 已有塔台 = 我的状态.塔台 || [];
  const 已占 = new Set(已有塔台.map(t => {
    const match = t.match(/@\((\d+),(\d+)\)/);
    return match ? `${match[1]},${match[2]}` : null;
  }).filter(Boolean));

  // 优先中间行（覆盖最大路径），优先两侧交替
  const 优先行 = [5, 6, 4, 7, 3, 8, 2, 9];
  for (const 行 of 优先行) {
    for (const 列 of 塔台槽列) {
      if (!已占.has(`${行},${列}`)) return { 行, 列 };
    }
  }
  return null;
}
