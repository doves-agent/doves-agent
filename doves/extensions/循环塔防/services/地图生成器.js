/**
 * 循环塔防 - 地图生成器
 * 根据玩家数生成环形走廊的段落布局
 */

import { 游戏常量 } from '../tools/_配置表.js';

const { 段落宽度, 段落高度, 塔台槽列, 路径列范围 } = 游戏常量;

/**
 * 生成地图 - 为每个玩家创建一个段落
 * @param {number} 玩家数
 * @returns {{ 段落: Array<段落数据> }}
 */
export function 生成地图(玩家数) {
  const 段落列表 = [];

  for (let i = 0; i < 玩家数; i++) {
    段落列表.push(创建段落(i));
  }

  return { 段落: 段落列表 };
}

/**
 * 创建单个段落
 * 段落是一个 宽×高 的网格：
 *   列0 和 列6 是塔台槽位
 *   列1-5 是单位行进路径
 *   行0 是入口（敌军进入处）
 *   行11 是出口（我方兵出发处）
 */
function 创建段落(槽位) {
  const 网格 = [];
  for (let 行 = 0; 行 < 段落高度; 行++) {
    const 行数据 = [];
    for (let 列 = 0; 列 < 段落宽度; 列++) {
      if (塔台槽列.includes(列) && 行 > 0 && 行 < 段落高度 - 1) {
        行数据.push({ 类型: '塔位', 占用: null });
      } else if (列 >= 路径列范围[0] && 列 <= 路径列范围[1]) {
        行数据.push({ 类型: '路径' });
      } else {
        行数据.push({ 类型: '边界' });
      }
    }
    网格.push(行数据);
  }

  return {
    槽位,
    网格,
    塔位列表: 获取塔位坐标(),
  };
}

/**
 * 获取所有可建塔位的坐标
 */
function 获取塔位坐标() {
  const 坐标 = [];
  for (let 行 = 1; 行 < 段落高度 - 1; 行++) {
    for (const 列 of 塔台槽列) {
      坐标.push({ 行, 列 });
    }
  }
  return 坐标;
}

/**
 * 获取单位行进路径（从入口到出口的行坐标序列）
 * 单位沿中间列直线行进，从行0到行(段落高度-1)
 */
export function 获取行进路径() {
  const 路径 = [];
  const 中列 = Math.floor((路径列范围[0] + 路径列范围[1]) / 2);
  for (let 行 = 0; 行 < 段落高度; 行++) {
    路径.push({ 行, 列: 中列 });
  }
  return 路径;
}

/**
 * 计算两点间距离（用于塔台射程判定）
 */
export function 计算距离(a, b) {
  return Math.sqrt((a.行 - b.行) ** 2 + (a.列 - b.列) ** 2);
}

/**
 * 验证塔台建造位置是否合法
 */
export function 验证建造位置(段落状态, 位置) {
  const { 行, 列 } = 位置;
  if (行 < 1 || 行 >= 段落高度 - 1) return false;
  if (!塔台槽列.includes(列)) return false;
  // 检查是否已被占用
  const 已有塔 = 段落状态.塔台.find(t => t.位置.行 === 行 && t.位置.列 === 列);
  return !已有塔;
}
