/**
 * 电脑操作员执行增强
 * 操作前截图了解状态，操作后验证结果
 */
export default {
  conditionalPrompts: [
    {
      match: (任务, tools) => {
        const 能力需求 = 任务.能力需求 || [];
        return 能力需求.some(a => ['电脑操作', '键鼠控制', '截图', '窗口管理', '进程控制', '桌面自动化'].includes(a));
      },
      prompt: `【电脑操作工具优先级】
- 截图 → 全屏截图
- 窗口列表 → 窗口列表
- 窗口查找 → 窗口查找
- 窗口激活 → 窗口激活
- 窗口关闭 → 窗口关闭（需确认）
- 输入文本 → 键盘输入
- 组合键 → 快捷键
- 鼠标点击 → 鼠标点击
- 鼠标移动 → 鼠标移动
- 鼠标滚动 → 鼠标滚轮
- 鼠标拖拽 → 鼠标拖拽
- 鼠标位置 → 鼠标位置
- 键盘按键 → 键盘按键
- 列出进程 → 进程列表
- 查找进程 → 进程查找
- 启动进程 → 启动进程
- 终止进程 → 终止进程（需确认）
- 执行命令 → 执行命令（需确认）

【安全规则】
- 操作前先用 全屏截图 了解当前状态
- 终止进程前必须使用 询问用户 向用户确认
- 关闭窗口前必须使用 询问用户 向用户确认
- 执行系统命令前必须使用 询问用户 向用户确认
- **批量确认**：需要终止多个进程/关闭多个窗口时，应一次性用 询问用户 列出所有目标让用户确认，而非逐个确认。例如用户说"关掉计算器和记事本"，应先 进程查找 查找所有目标进程，然后一次 询问用户 列出全部让用户确认
- 输入操作后可用 全屏截图 验证结果
- 窗口操作后等待 200-500ms 让窗口稳定

【进程/窗口查找规则】
- 进程查找/窗口查找 返回 count=0 时，目标可能不存在或名称不匹配
- macOS 上应用进程名通常为英文（如 Calculator 而非 计算器），中文搜索失败时尝试英文名
- 查找失败后如实告知用户"未找到运行中的XXX"，禁止编造如"模块缺失"、"依赖缺失"等虚假系统错误
- 确认目标不存在后，可用 启动进程 或 执行命令 帮用户启动应用`,
    },
  ],

  hooks: {
    afterToolCall: async (工具名, 工具结果, 任务) => {
      if (工具名 === '全屏截图' && !工具结果.isError) {
        任务.lastScreenshotTaken = true;
      }
      return null;
    },
  },
};
