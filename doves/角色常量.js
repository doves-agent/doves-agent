// ==================== 子任务角色系统 ====================

/**
 * 子任务角色（role）
 * 纯提示词层面的角色感知，不影响执行路径。
 * 每个子任务仍然统一走 LLM + 工具调用，
 * 但 system prompt 会根据角色注入不同的身份提示，
 * 让 LLM 更精确地理解自己在 Branch 中扮演什么角色。
 *
 * 设计原则：
 * - role 不做硬性分叉，所有子任务执行方式一致
 * - role 只是给 LLM 的推荐和指引
 * - 不传 role 就走默认的"智能助手"提示
 * - 定时/监控类需求走事件系统，不属于子任务角色
 */
export const 子任务角色 = {
  信息收集: '信息收集',    // 信息收集员：专注获取信息，不做主观判断
  推理分析: '推理分析',      // 分析员：对已有信息进行推理、对比、判断
  聚合汇总: '聚合汇总',   // 整合者：将多个子任务结果合并为统一输出
  验证质控: '验证质控',    // 验证员：检查其他子任务的输出质量和一致性
  创作执行: '创作执行',      // 执行者/创作者：按指令直接执行操作或创作内容
};

// ==================== 动态角色注册系统（扩展包支持） ====================

// 内置子任务角色值
const _内置子任务角色 = ['信息收集', '推理分析', '聚合汇总', '验证质控', '创作执行'];
// 扩展包追加的角色值：Map<角色名, 扩展包名>
const _扩展子任务角色 = new Map();

/**
 * 合法的子任务角色值（动态，支持扩展包追加）
 * 使用 getter 函数，比 Proxy 更简单、可调试、可静态分析
 */
export function 获取合法子任务角色() {
  return [..._内置子任务角色, ..._扩展子任务角色.keys()];
}

// 合法角色验证：检查角色是否在内置或扩展列表中
export function 是否合法角色(角色名) {
  return _内置子任务角色.includes(角色名) || _扩展子任务角色.has(角色名);
}

// 提供对象形式的接口（includes/length/迭代），内部不用 Proxy
export const 合法子任务角色 = {
  includes: (val) => _内置子任务角色.includes(val) || _扩展子任务角色.has(val),
  get length() { return _内置子任务角色.length + _扩展子任务角色.size; },
  [Symbol.iterator]: function*() { yield* _内置子任务角色; yield* _扩展子任务角色.keys(); },
};

/**
 * 注册扩展角色（供扩展加载器调用）
 */
export function 注册扩展角色(角色名, 扩展包名) {
  _扩展子任务角色.set(角色名, 扩展包名);
}

/**
 * 注销扩展角色（供扩展加载器卸载时调用）
 */
export function 注销扩展角色(扩展包名) {
  for (const [角色, 来源] of _扩展子任务角色) {
    if (来源 === 扩展包名) _扩展子任务角色.delete(角色);
  }
}

