// 扩展角色提示：Map<角色名, { 提示, 扩展包名 }>
const _扩展角色提示 = new Map();

/**
 * 角色提示映射（动态，支持扩展包追加）
 * 使用普通对象 + getter 函数，比 Proxy 更简单、可调试
 */
export const 角色提示映射 = {
  // 内置角色提示直接在对象上
  ..._内置角色提示,
  // 动态获取：先查内置，再查扩展
  get(角色名) {
    if (角色名 in _内置角色提示) return _内置角色提示[角色名];
    if (_扩展角色提示.has(角色名)) return _扩展角色提示.get(角色名).提示;
    return undefined;
  },
  has(角色名) {
    return 角色名 in _内置角色提示 || _扩展角色提示.has(角色名);
  },
  keys() {
    return [...Reflect.ownKeys(_内置角色提示), ..._扩展角色提示.keys()];
  },
};

/**
 * 注册扩展角色提示（供扩展加载器调用）
 */
export function 注册扩展角色提示(角色名, 提示, 扩展包名) {
  _扩展角色提示.set(角色名, { 提示, 扩展包名 });
}

/**
 * 注销扩展角色提示（供扩展加载器卸载时调用）
 */
export function 注销扩展角色提示(扩展包名) {
  for (const [角色, 数据] of _扩展角色提示) {
    if (数据.扩展包名 === 扩展包名) _扩展角色提示.delete(角色);
  }
}
