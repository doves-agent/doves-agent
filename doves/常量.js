// ==================== 执行模式说明 ====================
// 执行模式决定了 Branch 下子任务的调度方式：
// - 先规划后执行: 先拆解后执行，按拓扑排序阶段并发执行（默认）
// - 边做边规划: 交互式执行，每批后综合理解，动态调整计划
// - 管线式: 管线式执行，严格按阶段顺序，前阶段输出注入后阶段
// - 串行保障: 串行保障执行，严格顺序+验证门控+鸽子绑定，适合GUI自动化
// - 直接执行: 直接执行，不拆分，Branch自己完成

/**
 * 根据拆分深度获取任务类型
 * 深度0 → 'subtask'，深度1 → 'subtask_d1'，深度2 → 'subtask_d2'...
 * @param {number} 深度 - 拆分深度
 * @returns {string} 任务类型字符串
 */
export function 获取深度任务类型(深度) {
  if (深度 <= 0) return 任务类型.子任务;
  return `subtask_d${深度}`;
}

/**
 * 判断任务类型是否为子任务（含所有深度层级）
 * @param {string} 类型 - 任务类型字符串
 * @returns {boolean}
 */
export function 是否子任务类型(类型) {
  return 类型 === 任务类型.子任务 || 类型.startsWith('subtask_d');
}

