// 扩展策略意图：Map<意图名, 扩展包名>
const _扩展策略意图 = new Map();

/**
 * 合法策略意图（动态，支持扩展包追加）
 * 使用普通对象 + getter 函数，比 Proxy 更简单
 */
export const 合法策略意图 = {
  includes: (val) => _内置策略意图.includes(val) || _扩展策略意图.has(val),
  get length() { return _内置策略意图.length + _扩展策略意图.size; },
  [Symbol.iterator]: function*() { yield* _内置策略意图; yield* _扩展策略意图.keys(); },
};

/**
 * 注册扩展策略意图（供扩展加载器调用）
 * @param {string} 意图名 - 策略意图标识
 * @param {string} 扩展包名 - 来源扩展包
 */
export function 注册扩展策略意图(意图名, 扩展包名) {
  _扩展策略意图.set(意图名, 扩展包名);
}

/**
 * 注销扩展策略意图（供扩展加载器卸载时调用）
 * @param {string} 扩展包名 - 来源扩展包
 */
export function 注销扩展策略意图(扩展包名) {
  for (const [意图, 来源] of _扩展策略意图) {
    if (来源 === 扩展包名) _扩展策略意图.delete(意图);
  }
}

