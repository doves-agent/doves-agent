/**
 * 日志规范模块
 * 
 * 【KISS原则文档的一部分】
 * 本文件集中定义项目日志规范，避免文档与代码分离
 * 
 * === 一、统一日志系统 ===
 * 
 * 所有 Node.js 运行时代码必须使用 创建日志器 统一日志系统：
 *   import { 创建日志器 } from '../common/日志管理器.js';
 *   const logger = 创建日志器('模块名', { 前缀: '[模块名]', 级别: 'debug', 显示调用位置: true });
 * 
 * 禁止事项：
 * ❌ console.log / console.warn / console.error / console.debug — 直接用于日志记录
 * ❌ 手动 logger 对象 — 如 const logger = { info: (...a) => console.log('[xxx]', ...a) }
 * ❌ if (!是否静默()) console.xxx — 创建日志器内置级别过滤，无需手动守卫
 * ❌ process.env.DEBUG && console.log — 使用 logger.debug() 替代
 * 
 * 允许直接使用 console 的场景：
 * ✅ CLI 帮助文本（如 --help 输出）
 * ✅ 结构化终端输出（如 llm-logger.js 的 LLM 调用信息框）
 * ✅ 浏览器端代码（web/*.html / web/*.js 中的 <script>）
 * ✅ 构建脚本（scripts/build.js）
 * 
 * === 二、创建日志器 API ===
 * 
 * 创建日志器(模块名, 选项) → 日志管理器实例（单例）
 * 
 * 参数说明：
 * ┌──────────────┬──────────┬────────────────────────────────────┐
 * │ 参数         │ 默认值    │ 说明                               │
 * ├──────────────┼──────────┼────────────────────────────────────┤
 * │ 模块名       │ (必填)    │ 模块标识，同时作为日志目录名       │
 * │ 选项.前缀    │ [模块名]  │ 终端输出的前缀标识                 │
 * │ 选项.级别    │ 'info'   │ 最低输出级别: debug/info/warn/error│
 * │ 选项.显示调用位置 │ false │ 始终显示 file:line:col 可点击链接  │
 * │ 选项.日志根目录   │ ~/.dove/logs │ 日志文件根目录            │
 * │ 选项.保留天数 │ 30       │ 日志文件保留天数                   │
 * │ 选项.终端输出 │ true     │ 是否输出到终端                     │
 * │ 选项.文件输出 │ true     │ 是否输出到日志文件                 │
 * └──────────────┴──────────┴────────────────────────────────────┘
 * 
 * 日志方法：
 *   logger.info(...args)   — 常规信息
 *   logger.warn(...args)   — 警告
 *   logger.error(...args)  — 错误
 *   logger.debug(...args)  — 调试信息
 * 
 * === 三、模块命名规范 ===
 * 
 * 模块名即日志器缓存键（单例），同一模块名只创建一次实例。
 * 
 * 命名约定：
 * ┌───────────────────┬──────────────────────┬──────────────────────────┐
 * │ 文件位置          │ 模块名               │ 前缀                     │
 * ├───────────────────┼──────────────────────┼──────────────────────────┤
 * │ doves/根目录      │ 功能名               │ [功能名]                 │
 * │ doves/extensions/ │ 扩展名或扩展/子功能  │ [扩展名] 或 [子模块标识] │
 * │ doves/tools/      │ 工具名               │ [工具名]                 │
 * │ doves/skills/     │ 技能名               │ [技能名]                 │
 * │ doves/common/     │ 通用模块名           │ [模块名]                 │
 * │ server/           │ 服务模块名           │ [模块名]                 │
 * └───────────────────┴──────────────────────┴──────────────────────────┘
 * 
 * 示例：
 *   创建日志器('扩展加载器', { 前缀: '[扩展加载器]' })
 *   创建日志器('quality_gate', { 前缀: '[quality_gate]' })
 *   创建日志器('MongoDB工具', { 前缀: '[mongo_agent]' })  — 可保留英文缩写前缀
 *   创建日志器('游戏服务器', { 前缀: '[游戏服务器]' })
 * 
 * === 四、级别使用规范 ===
 * 
 * ┌──────────┬────────────────────────────────────────────────────────┐
 * │ 级别     │ 使用场景                                              │
 * ├──────────┼────────────────────────────────────────────────────────┤
 * │ debug    │ 开发调试信息：函数入口/出口、变量值、流程分支选择      │
 * │ info     │ 常规运行信息：启动完成、连接建立、操作成功、状态变更  │
 * │ warn     │ 非致命异常：降级操作、重试、可选功能不可用、兼容警告  │
 * │ error    │ 错误与异常：操作失败、连接断开、数据异常              │
 * └──────────┴────────────────────────────────────────────────────────┘
 * 
 * 关键规则：
 * 1. 新模块统一设置 级别: 'debug'，确保调试信息可见
 * 2. 生产环境可通过 LOG_LEVEL 环境变量覆盖为 'info' 或 'warn'
 * 3. logger.error() 第二参数可传 Error 对象，自动输出 stack trace
 * 4. 禁止空 catch：至少 logger.warn() 记录异常
 * 
 * === 五、Import 路径规范 ===
 * 
 * 创建日志器的 import 路径按文件深度不同：
 * 
 * ┌─────────────────────────┬──────────────────────────────────────┐
 * │ 文件深度                │ import 路径                          │
 * ├─────────────────────────┼──────────────────────────────────────┤
 * │ doves/根目录文件        │ './common/日志管理器.js'            │
 * │ doves/xxx/ 一级子目录   │ '../common/日志管理器.js'           │
 * │ doves/xxx/yyy/ 二级     │ '../../common/日志管理器.js'        │
 * │ doves/xxx/yyy/zzz/ 三级 │ '../../../common/日志管理器.js'     │
 * │ doves/xxx/yyy/zzz/ww/   │ '../../../../common/日志管理器.js'  │
 * └─────────────────────────┴──────────────────────────────────────┘
 * 
 * === 六、日志文件结构 ===
 * 
 * 日志文件统一存储于 ~/.dove/logs/<模块名>/ 目录下：
 *   <模块名>.log          — 当天全部日志
 *   <模块名>-error.log    — 当天错误日志
 *   <模块名>.YYYY-MM-DD.log — 历史归档日志
 * 
 * 可通过 CLI 命令查看：
 *   dove log              — 查看所有模块日志
 *   dove log <模块名>     — 查看指定模块日志
 *   dove log --error      — 仅查看错误日志
 *   dove log --follow     — 实时跟踪日志（tail -f）
 * 
 * === 七、显示调用位置 ===
 * 
 * 设置 显示调用位置: true 时，每条日志都附带 file:line:col 链接：
 *   [扩展加载器] 15:30:45.123 F:\doves\白鸽系统\doves\extensions\_loader.js:42:8 扩展包加载...
 * 
 * 此链接在 VS Code 终端和 Windows Terminal 中可 Ctrl+点击跳转。
 * 
 * 环境变量 DOVE_DEBUG=1 也会全局启用调用位置显示（无需逐模块配置）。
 * 
 * === 八、迁移检查清单 ===
 * 
 * 新文件或重构时应确认：
 * ☑ 所有日志输出使用 创建日志器，无直接 console.xxx 调用
 * ☑ 无手动 logger 对象定义（const logger = { info: (...a) => console.log(...) }）
 * ☑ 无 是否静默() 守卫（创建日志器内置级别过滤）
 * ☑ 无 process.env.DEBUG && console.log 模式
 * ☑ import 路径正确（按目录深度计算相对路径）
 * ☑ 级别选择正确（debug/info/warn/error 按场景区分）
 * ☑ 浏览器端代码和CLI帮助文本例外，保持 console 直接输出
 */

export const 日志规范 = {
  统一日志器: '创建日志器',
  来源模块: 'common/日志管理器.js',
  禁止: {
    直接console调用: true,
    手动logger对象: true,
    是否静默守卫: true,
    DEBUG环境变量守卫: true,
  },
  允许console的场景: ['CLI帮助文本', '结构化终端输出', '浏览器端代码', '构建脚本'],
  默认级别: 'debug',
  生产级别: 'info',
  显示调用位置: true,
};

export default 日志规范;
