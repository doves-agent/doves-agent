/**
 * 开发规范模块
 * 
 * 【KISS原则文档的一部分】
 * 本文件集中定义项目开发规范，避免文档与代码分离
 * 
 * === 一、测试规范 ===
 * 
 * 核心原则：禁止使用第三方测试框架，只允许使用 Test 目录中的测试方法
 * 
 * 允许的测试方式：
 * - Test/模拟测试.js - 框架验证测试
 * - Test/网关测试.js - 网关功能测试
 * - Test/存储测试.js - 存储系统测试
 * - Test/链路测试.js - 端到端链路测试
 * 
 * 禁止事项：
 * ❌ 使用 Jest/Mocha/Jasmine 等测试框架
 * ❌ 使用 chai/sinon 等断言库
 * ❌ 使用复杂的测试工具链
 * ❌ 在 Test 目录外创建测试文件
 * 
 * 运行测试：
 * cd 白鸽系统/Test
 * node 模拟测试.js
 * node 网关测试.js
 * 
 * === 二、时间处理规范 ===
 * 
 * 核心规则：
 * 1. 禁止使用 UTC 时间 - 使用 toLocalISOString() 返回带时区的本地时间
 * 2. 时间索引必须使用毫秒级时间戳 - 每个时间字段同时存储两个字段
 * 
 * 字段命名规范：
 * ┌──────────────┬─────────────────┬─────────────────┐
 * │ 用途         │ 时间字符串      │ 时间戳索引      │
 * ├──────────────┼─────────────────┼─────────────────┤
 * │ 创建时间     │ createdAt       │ createdAtTs     │
 * │ 更新时间     │ updatedAt       │ updatedAtTs     │
 * │ 完成时间     │ completedAt     │ completedAtTs   │
 * │ 心跳时间     │ heartbeatAt     │ heartbeatAtTs   │
 * │ 失败时间     │ failedAt        │ failedAtTs      │
 * │ 过期时间     │ expiresAt       │ expiresAtTs     │
 * └──────────────┴─────────────────┴─────────────────┘
 * 
 * 使用时间工具模块：
 * import { toLocalISOString, getTimestamp, createTimestampFields } from '../common/时间工具.js';
 * 
 * const { localTime, timestamp } = createTimestampFields();
 * // localTime: "2026-04-01T15:30:45.123+08:00"
 * // timestamp: 1711961445123
 * 
 * === 三、依赖安全策略（最高优先级） ===
 * 
 * 核心原则：所有依赖库和模块必须使用固定版本
 * 
 * 策略要求：
 * ┌──────────────────────┬────────────────────────────────────┐
 * │ 规则                 │ 说明                               │
 * ├──────────────────────┼────────────────────────────────────┤
 * │ 禁止使用 ^ 或 ~      │ 必须使用精确版本号                 │
 * │ 优先选择 AI 前版本   │ 2022 年前发布的版本更稳定         │
 * │ 新版本评估周期       │ 新版本发布后至少观察 3-6 个月     │
 * │ 安全审计             │ 每次更新前运行 npm audit          │
 * │ 锁定文件提交         │ package-lock.json 必须提交        │
 * └──────────────────────┴────────────────────────────────────┘
 * 
 * 禁止行为：
 * ❌ npm install package@latest 直接安装
 * ❌ 在 package.json 中使用 *、^、~ 通配符
 * ❌ 未经审计直接升级依赖
 * ❌ 使用维护者失联超过 6 个月的库
 * 
 * === 四、开发标注要求 ===
 * 
 * 所有代码开发必须遵循以下标注规范：
 * 
 * ┌──────────────┬────────────────────────────────────┐
 * │ 标注项       │ 示例                              │
 * ├──────────────┼────────────────────────────────────┤
 * │ 新增功能     │ // 新增: 用户认证功能             │
 * │ 修改内容     │ // 修改: 修复任务队列抢锁逻辑     │
 * │ 待办事项     │ // TODO: 待实现流式响应           │
 * │ 已知问题     │ // FIXME: 并发时可能存在问题      │
 * └──────────────┴────────────────────────────────────┘
 * 
 * === 五、代码注释规范 ===
 * 
 * 重要规范说明必须标注"KISS原则文档的一部分"
 * 示例：* 【KISS原则文档的一部分】子任务拆解规则...
 */

