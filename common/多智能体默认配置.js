/**
 * 获取完整的默认配置对象
 * @returns {Object} 完整的多智能体默认配置
 */
export function 获取默认配置() {
  return {
    主智能体角色名: 默认主智能体角色名,
    智能体列表: 默认智能体列表.map(a => ({ ...a })),
  };
}

/**
 * 验证配置是否合法
 * @param {Object} 配置 - 多智能体配置对象
 * @returns {{ 合法: boolean, 错误: string|null }}
 */
export function 验证配置(配置) {
  if (!配置 || typeof 配置 !== 'object') {
    return { 合法: false, 错误: '配置对象为空或格式错误' };
  }
  if (!Array.isArray(配置.智能体列表) || 配置.智能体列表.length === 0) {
    return { 合法: false, 错误: '智能体列表不能为空' };
  }

  const 主智能体列表 = 配置.智能体列表.filter(a => a.是否主智能体);
  if (主智能体列表.length === 0) {
    return { 合法: false, 错误: '缺少主智能体' };
  }
  if (主智能体列表.length > 1) {
    return { 合法: false, 错误: '主智能体不能有多个' };
  }

  for (const agent of 配置.智能体列表) {
    if (!agent.角色名) {
      return { 合法: false, 错误: '智能体缺少角色名' };
    }
    if (!agent.模型名) {
      return { 合法: false, 错误: `智能体"${agent.角色名}"缺少模型名` };
    }
  }

  return { 合法: true, 错误: null };
}

export default {
  默认主智能体角色名,
  默认智能体列表,
  获取默认配置,
  验证配置,
};
