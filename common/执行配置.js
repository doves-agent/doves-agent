/**
 * 执行配置管理器
 * 负责配置的加载、合并、校验
 */
export class 执行配置管理器 {
  constructor() {
    // 内置配置索引（按标识快速查找）
    this.配置索引 = new Map();
    for (const 配置 of 内置配置列表) {
      this.配置索引.set(配置.标识, 配置);
    }
    // 数据库加载的自定义配置缓存
    this.自定义配置缓存 = new Map();
    this.缓存过期时间 = 0;
  }

  /**
   * 解析执行配置：Profile标识 + 内联覆盖 → 最终生效配置
   * @param {string|null} profile标识 - Profile 标识（如 'crawler'）
   * @param {Object} 内联参数 - CLI 传入的内联覆盖参数
   * @returns {Object} 最终生效的执行配置
   */
  解析(profile标识, 内联参数 = {}) {
    // Step 1: 获取基础配置
    let 基础配置;
    if (profile标识) {
      基础配置 = this.获取配置(profile标识);
      if (!基础配置) {
        console.warn(`[执行配置] 未找到配置 "${profile标识}"，使用默认配置`);
        基础配置 = this.获取配置('default') || 内置配置列表[0];
      }
    } else {
      基础配置 = 内置配置列表[0]; // default
    }

    // Step 2: 提取约束部分
    const 最终配置 = {
      执行约束: { ...默认空约束.执行约束, ...基础配置.执行约束 },
      能力约束: { ...默认空约束.能力约束, ...基础配置.能力约束 },
      工具约束: { ...默认空约束.工具约束, ...基础配置.工具约束 },
      技能约束: { ...默认空约束.技能约束, ...基础配置.技能约束 },
      意图约束: { ...默认空约束.意图约束, ...基础配置.意图约束 },
      _来源: {
        profile: profile标识 || 'default',
        内联参数: Object.keys(内联参数).length > 0,
      },
    };

    // Step 3: 应用内联覆盖
    this._应用内联覆盖(最终配置, 内联参数);

    return 最终配置;
  }

  /**
   * 从内置或缓存中获取配置
   * @param {string} 标识 - Profile 标识
   * @returns {Object|null} 配置对象
   */
  获取配置(标识) {
    // 优先查内置
    if (this.配置索引.has(标识)) {
      return this.配置索引.get(标识);
    }
    // 查自定义缓存
    if (this.自定义配置缓存.has(标识)) {
      return this.自定义配置缓存.get(标识);
    }
    return null;
  }

  /**
   * 从数据库加载自定义配置
   * @param {Object} 数据库连接 - MongoDB 连接
   * @param {string} 用户数据库名 - 数据库名
   */
  async 加载数据库配置(数据库连接, 用户数据库名) {
    try {
      const db = 数据库连接.db(用户数据库名);
      const 集合 = db.collection('执行配置');
      const 自定义列表 = await 集合.find({ 是否内置: false }).toArray();
      
      for (const 配置 of 自定义列表) {
        this.自定义配置缓存.set(配置.标识, 配置);
      }
      
      console.log(`[执行配置管理器] 从数据库加载了 ${自定义列表.length} 个自定义配置`);
    } catch (e) {
      console.warn(`[执行配置管理器] 加载数据库配置失败: ${e.message}`);
    }
  }

  /**
   * 初始化内置配置到数据库（幂等操作）
   * @param {Object} 数据库连接 - MongoDB 连接
   * @param {string} 用户数据库名 - 数据库名
   */
  async 初始化内置配置(数据库连接, 用户数据库名) {
    try {
      const db = 数据库连接.db(用户数据库名);
      const 集合 = db.collection('执行配置');

      for (const 配置 of 内置配置列表) {
        const ts = createTimestampFields();
        await 集合.updateOne(
          { 标识: 配置.标识 },
          { $setOnInsert: { ...配置, 创建时间: ts.localTime, 创建时间戳: ts.timestamp } },
          { upsert: true }
        );
      }
      
      console.log(`[执行配置管理器] 初始化 ${内置配置列表.length} 个内置配置`);
    } catch (e) {
      console.warn(`[执行配置管理器] 初始化内置配置失败: ${e.message}`);
    }
  }

  /**
   * 列出所有可用配置
   * @returns {Array} 配置摘要列表
   */
  列出所有() {
    const 结果 = [];
    for (const 配置 of 内置配置列表) {
      结果.push({
        标识: 配置.标识,
        名称: 配置.名称,
        描述: 配置.描述,
        标签: 配置.标签,
        是否内置: true,
      });
    }
    for (const [标识, 配置] of this.自定义配置缓存) {
      结果.push({
        标识: 配置.标识,
        名称: 配置.名称,
        描述: 配置.描述,
        标签: 配置.标签 || [],
        是否内置: false,
      });
    }
    return 结果;
  }

  /**
   * 按标签搜索配置
   * @param {string} 标签 - 搜索标签
   * @returns {Array} 匹配的配置列表
   */
  按标签搜索(标签) {
    return this.列出所有().filter(c => c.标签.includes(标签));
  }

  /**
   * 应用内联覆盖参数到最终配置
   * @private
   * @param {Object} 最终配置 - 当前配置（会被原地修改）
   * @param {Object} 内联参数 - CLI 传入的覆盖参数
   */
  _应用内联覆盖(最终配置, 内联参数) {
    if (!内联参数 || Object.keys(内联参数).length === 0) return;

    // 执行约束覆盖
    if (内联参数.禁止拆分 !== undefined) 最终配置.执行约束.禁止拆分 = !!内联参数.禁止拆分;
    if (内联参数.禁止并行 !== undefined) 最终配置.执行约束.禁止并行 = !!内联参数.禁止并行;
    if (内联参数.禁止递归 !== undefined) 最终配置.执行约束.禁止递归 = !!内联参数.禁止递归;
    if (内联参数.最大并发数 !== undefined) 最终配置.执行约束.最大并发数 = Number(内联参数.最大并发数);
    if (内联参数.最大拆分深度 !== undefined) 最终配置.执行约束.最大拆分深度 = Number(内联参数.最大拆分深度);
    if (内联参数.建议拆分 !== undefined) 最终配置.执行约束.建议拆分 = !!内联参数.建议拆分;
    if (内联参数.建议规划再执行 !== undefined) 最终配置.执行约束.建议规划再执行 = !!内联参数.建议规划再执行;
    if (内联参数.执行模式覆盖 !== undefined) 最终配置.执行约束.执行模式覆盖 = 内联参数.执行模式覆盖;

    // 能力约束追加（内联参数追加到Profile预设，而非覆盖）
    if (内联参数.可用能力) 最终配置.能力约束.可用能力 = [...最终配置.能力约束.可用能力, ...this._解析逗号列表(内联参数.可用能力)];
    if (内联参数.禁用能力) 最终配置.能力约束.禁用能力 = [...最终配置.能力约束.禁用能力, ...this._解析逗号列表(内联参数.禁用能力)];
    if (内联参数.建议能力) 最终配置.能力约束.建议能力 = [...最终配置.能力约束.建议能力, ...this._解析逗号列表(内联参数.建议能力)];

    // 工具约束追加（同上，内联参数追加而非覆盖）
    if (内联参数.工具安全级别上限) 最终配置.工具约束.工具安全级别上限 = 内联参数.工具安全级别上限;
    if (内联参数.可用工具) 最终配置.工具约束.可用工具 = [...最终配置.工具约束.可用工具, ...this._解析逗号列表(内联参数.可用工具)];
    if (内联参数.禁用工具) 最终配置.工具约束.禁用工具 = [...最终配置.工具约束.禁用工具, ...this._解析逗号列表(内联参数.禁用工具)];

    // 技能约束追加
    if (内联参数.可用技能) 最终配置.技能约束.可用技能 = [...最终配置.技能约束.可用技能, ...this._解析逗号列表(内联参数.可用技能)];
    if (内联参数.禁用技能) 最终配置.技能约束.禁用技能 = [...最终配置.技能约束.禁用技能, ...this._解析逗号列表(内联参数.禁用技能)];

    // 意图约束：强制意图覆盖（单值），禁止意图追加（列表）
    if (内联参数.强制意图) 最终配置.意图约束.强制意图 = 内联参数.强制意图;
    if (内联参数.禁止意图) 最终配置.意图约束.禁止意图 = [...最终配置.意图约束.禁止意图, ...this._解析逗号列表(内联参数.禁止意图)];
    if (内联参数.禁止闪回 !== undefined) 最终配置.意图约束.禁止闪回 = !!内联参数.禁止闪回;

    // 禁止拆分时自动调整执行模式
    if (最终配置.执行约束.禁止拆分 && !内联参数.执行模式覆盖) {
      最终配置.执行约束.执行模式覆盖 = '直接执行';
    }

    // 禁止并行时自动调整并发数
    if (最终配置.执行约束.禁止并行 && !内联参数.最大并发数) {
      最终配置.执行约束.最大并发数 = 1;
    }
  }

  /**
   * 解析逗号分隔的列表字符串
   * @private
   * @param {string|Array} 值 - 逗号分隔字符串或数组
   * @returns {Array} 字符串数组
   */
  _解析逗号列表(值) {
    if (Array.isArray(值)) return 值;
    if (typeof 值 === 'string') return 值.split(',').map(s => s.trim()).filter(Boolean);
    return [];
  }

  /**
   * 校验执行配置是否合法
   * @param {Object} 配置 - 待校验的配置
   * @returns {Object} { 有效, 错误列表 }
   */
  校验(配置) {
    const 错误列表 = [];
    const _合法执行模式 = [...合法执行模式, null]; // 校验时 null 表示未设置
    const _合法安全级别 = [...合法安全级别, null];

    if (配置.执行约束) {
      if (配置.执行约束.执行模式覆盖 && !_合法执行模式.includes(配置.执行约束.执行模式覆盖)) {
        错误列表.push(`执行模式覆盖无效: ${配置.执行约束.执行模式覆盖}`);
      }
      if (配置.执行约束.最大并发数 !== null && (配置.执行约束.最大并发数 < 1 || 配置.执行约束.最大并发数 > 200)) {
        错误列表.push(`最大并发数超出范围: ${配置.执行约束.最大并发数} (1-200)`);
      }
      if (配置.执行约束.最大拆分深度 !== null && (配置.执行约束.最大拆分深度 < 0 || 配置.执行约束.最大拆分深度 > 5)) {
        错误列表.push(`最大拆分深度超出范围: ${配置.执行约束.最大拆分深度} (0-5)`);
      }
    }

    if (配置.工具约束) {
      if (配置.工具约束.工具安全级别上限 && !_合法安全级别.includes(配置.工具约束.工具安全级别上限)) {
        错误列表.push(`工具安全级别上限无效: ${配置.工具约束.工具安全级别上限}`);
      }
    }

    return { 有效: 错误列表.length === 0, 错误列表 };
  }

  /**
   * 从 CLI 选项中提取执行配置参数
   * @param {Object} options - Commander 选项对象
   * @returns {Object} { profile, 内联参数 }
   */
  static 从CLI选项提取(options) {
    const profile = options.profile || null;
    const 内联参数 = {};

    // 执行约束（Commander --no-X 选项生成 options.X=false，非 options.noX=true）
    if (options.split === false) 内联参数.禁止拆分 = true;
    if (options.parallel === false) 内联参数.禁止并行 = true;
    if (options.flash === false) 内联参数.禁止闪回 = true;
    if (options.maxConcurrency) 内联参数.最大并发数 = parseInt(options.maxConcurrency);
    if (options.maxDepth) 内联参数.最大拆分深度 = parseInt(options.maxDepth);
    if (options.executionMode) 内联参数.执行模式覆盖 = options.executionMode;
    if (options.planFirst) 内联参数.建议规划再执行 = true;

    // 工具约束
    if (options.toolLevel) 内联参数.工具安全级别上限 = options.toolLevel;
    if (options.enableAbility) 内联参数.建议能力 = options.enableAbility;
    if (options.disableAbility) 内联参数.禁用能力 = options.disableAbility;
    if (options.disableTool) 内联参数.禁用工具 = options.disableTool;

    return { profile, 内联参数 };
  }
}

export default 执行配置管理器;
